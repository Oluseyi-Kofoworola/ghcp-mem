#!/usr/bin/env node
/* eslint-disable no-console */
/**
 * v2.0.0 mass-rename helper: ghcp-mem → baton.
 *
 * Three identifier flavours are renamed across the repo:
 *
 *   ghcp-mem    →  baton-mem           (npm package name, marketplace itemName,
 *                                        bin entries, repository URLs)
 *   ghcpMem     →  baton               (command IDs, settings keys, MCP tool
 *                                        prefixes — anything camelCase)
 *   GHCP-MEM    →  Baton               (display strings, doc/UI copy)
 *
 * Plus:
 *   ghcp-mem-mcp           →  baton-mem-mcp           (bin entry)
 *   ghcp-mem-ci-seed       →  baton-mem-ci-seed       (bin entry)
 *   ghcpMem_search etc.    →  baton_search etc.       (MCP tool method names)
 *   "ghcp-mem" participant →  "baton" participant     (chat participant id;
 *                                                      legacy id kept aliased
 *                                                      at activation time)
 *
 * Scope: every tracked file under the repo EXCEPT:
 *   - node_modules, out, out-test, dist, .git (machine-generated / vendored)
 *   - this script itself
 *   - the v2.0.0 CHANGELOG entry (which must mention the OLD names verbatim
 *     to be a useful migration guide — handled by string-marker exclusion)
 *
 * The script is idempotent: running it twice is a no-op the second time.
 */

import { readFileSync, writeFileSync, statSync, readdirSync } from 'node:fs';
import { join, relative, resolve, sep } from 'node:path';

const ROOT = resolve(process.argv[2] ?? '.');
const DRY_RUN = process.argv.includes('--dry-run');
const VERBOSE = process.argv.includes('--verbose');

const EXCLUDED_DIRS = new Set([
  'node_modules',
  'out',
  'out-test',
  'dist',
  '.git',
  '.vscode-test',
]);

const EXCLUDED_FILES = new Set([
  'package-lock.json', // regenerated by npm
  'rename-to-baton.mjs', // this file
]);

const INCLUDED_EXTS = new Set([
  '.ts',
  '.js',
  '.mjs',
  '.cjs',
  '.json',
  '.md',
  '.yml',
  '.yaml',
  '.html',
  '.css',
]);

// IMPORTANT: order matters. More specific patterns first so we don't
// double-replace. The replacements are designed to compose correctly:
// 'ghcp-mem-mcp' → 'baton-mem-mcp' must happen BEFORE 'ghcp-mem' → 'baton-mem'.
const REPLACEMENTS = [
  // ── Most specific compound identifiers first ─────────────────────────
  ['ghcp-mem-mcp', 'baton-mem-mcp'],
  ['ghcp-mem-ci-seed', 'baton-mem-ci-seed'],

  // ── MCP tool names (camelCase prefix) ────────────────────────────────
  ['ghcpMem_search', 'baton_search'],
  ['ghcpMem_recent', 'baton_recent'],
  ['ghcpMem_timeline', 'baton_timeline'],
  ['ghcpMem_get', 'baton_get'],
  ['ghcpMem_route', 'baton_route'],
  ['ghcpMem_entity', 'baton_entity'],
  ['ghcpMem_snippets', 'baton_snippets'],
  ['ghcpMem_lineage', 'baton_lineage'],
  ['ghcpMem_explain', 'baton_explain'],
  ['ghcpMem_store', 'baton_store'],
  ['ghcpMem_delete', 'baton_delete'],
  ['ghcpMem_correct', 'baton_correct'],
  ['ghcpMem_retract', 'baton_retract'],
  ['ghcpMem_supersede', 'baton_supersede'],
  ['ghcpMem_health', 'baton_health'],

  // ── Display strings ───────────────────────────────────────────────────
  ['GHCP-MEM', 'Baton'],

  // ── kebab-case (package name, bin, repo URL, marketplace itemName) ───
  ['ghcp-mem', 'baton-mem'],

  // ── camelCase (settings keys, command IDs) ────────────────────────────
  ['ghcpMem', 'baton'],
];

// Files where a literal occurrence of an old identifier is INTENTIONAL
// and must be preserved (e.g. the v2.0.0 migration guide in CHANGELOG.md).
// Detected via a marker comment so we don't have to hardcode line numbers.
const PRESERVE_MARKER_START = '<!-- baton:preserve-old-names -->';
const PRESERVE_MARKER_END = '<!-- /baton:preserve-old-names -->';

let filesScanned = 0;
let filesChanged = 0;
let totalReplacements = 0;
const perPatternCounts = Object.fromEntries(REPLACEMENTS.map(([k]) => [k, 0]));

function walk(dir) {
  const entries = readdirSync(dir, { withFileTypes: true });
  for (const e of entries) {
    if (e.name.startsWith('.') && e.name !== '.github' && e.name !== '.prettierrc.json' && e.name !== '.prettierignore' && e.name !== '.vscode' && e.name !== '.gitignore' && e.name !== '.gitattributes' && e.name !== '.vscodeignore') continue;
    if (EXCLUDED_DIRS.has(e.name)) continue;
    const full = join(dir, e.name);
    if (e.isDirectory()) {
      walk(full);
    } else if (e.isFile()) {
      if (EXCLUDED_FILES.has(e.name)) continue;
      const ext = e.name.includes('.') ? e.name.slice(e.name.lastIndexOf('.')) : '';
      if (!INCLUDED_EXTS.has(ext)) {
        // Always process top-level config-ish files even without an ext.
        if (!['CHANGELOG', 'CHANGELOG.md', 'README', 'README.md', 'LICENSE', 'AGENTS.md'].includes(e.name)) {
          continue;
        }
      }
      processFile(full);
    }
  }
}

function applyReplacementsRespectingMarkers(text) {
  // Split by preserve markers; only run replacements outside preserve blocks.
  const parts = [];
  let cursor = 0;
  let perFileReplacements = 0;
  while (cursor < text.length) {
    const startIdx = text.indexOf(PRESERVE_MARKER_START, cursor);
    if (startIdx === -1) {
      parts.push({ text: text.slice(cursor), rewrite: true });
      break;
    }
    parts.push({ text: text.slice(cursor, startIdx), rewrite: true });
    const endIdx = text.indexOf(PRESERVE_MARKER_END, startIdx);
    if (endIdx === -1) {
      // Unterminated marker — preserve the rest verbatim and warn.
      console.warn(`  ⚠️  unterminated ${PRESERVE_MARKER_START} marker`);
      parts.push({ text: text.slice(startIdx), rewrite: false });
      break;
    }
    parts.push({
      text: text.slice(startIdx, endIdx + PRESERVE_MARKER_END.length),
      rewrite: false,
    });
    cursor = endIdx + PRESERVE_MARKER_END.length;
  }

  const rewritten = parts.map((p) => {
    if (!p.rewrite) return p.text;
    let out = p.text;
    for (const [from, to] of REPLACEMENTS) {
      const before = out;
      out = out.split(from).join(to);
      if (out !== before) {
        const count = (before.match(new RegExp(escapeRe(from), 'g')) || []).length;
        perPatternCounts[from] += count;
        perFileReplacements += count;
      }
    }
    return out;
  });

  return { text: rewritten.join(''), replacements: perFileReplacements };
}

function escapeRe(s) {
  return s.replace(/[.*+?^${}()|[\]\\]/g, '\\$&');
}

function processFile(path) {
  filesScanned++;
  let original;
  try {
    original = readFileSync(path, 'utf8');
  } catch {
    return; // skip binary / unreadable
  }
  const { text: rewritten, replacements } = applyReplacementsRespectingMarkers(original);
  if (rewritten === original) {
    return;
  }
  filesChanged++;
  totalReplacements += replacements;
  if (VERBOSE) {
    console.log(`  ${relative(ROOT, path).split(sep).join('/')}  (+${replacements})`);
  }
  if (!DRY_RUN) {
    writeFileSync(path, rewritten);
  }
}

console.log(`Renaming ghcp-mem → baton in ${ROOT}${DRY_RUN ? '  [DRY RUN]' : ''}`);
walk(ROOT);
console.log('');
console.log(`Files scanned:       ${filesScanned}`);
console.log(`Files changed:       ${filesChanged}`);
console.log(`Total replacements:  ${totalReplacements}`);
console.log('');
console.log('Per-pattern counts:');
for (const [pat, n] of Object.entries(perPatternCounts)) {
  if (n > 0) {
    console.log(`  ${pat.padEnd(30)} → ${n}`);
  }
}
